@push('scripts')
    <script>
        // Data kategori dan jumlah produk dari controller
        var categoryLabels = @json($productCountsByCategory->pluck('category.category_name'));
        var categoryCounts = @json($productCountsByCategory->pluck('count'));

        // Menetapkan warna berdasarkan kategori
        var categoryColors = categoryLabels.map(function(category) {
            if (category === 'Clothes') {
                return '#4e73df'; // Biru untuk Clothes
            } else if (category === 'Shoes') {
                return '#1cc88a'; // Hijau untuk Shoes
            } else {
                return '#36b9cc'; // Warna default untuk kategori lainnya
            }
        });

        // Pie Chart Example
        var ctx = document.getElementById("myPieChart");
        var myPieChart = new Chart(ctx, {
            type: 'doughnut',
            data: {
                labels: categoryLabels, // Menampilkan nama kategori
                datasets: [{
                    data: categoryCounts, // Menampilkan jumlah produk dari kategori
                    backgroundColor: ['#4e73df', '#1cc88a', '#36b9cc', '#f6c23e', '#e74a3b'],
                    hoverBackgroundColor: ['#2e59d9', '#17a673', '#2c9faf', '#f4b619', '#e33c2d'],
                    hoverBorderColor: "rgba(234, 236, 244, 1)",
                }],
            },
            options: {
                maintainAspectRatio: false,
                tooltips: {
                    backgroundColor: "rgb(255,255,255)",
                    bodyFontColor: "#858796",
                    borderColor: '#dddfeb',
                    borderWidth: 1,
                    xPadding: 15,
                    yPadding: 15,
                    displayColors: false,
                    caretPadding: 10,
                },
                legend: {
                    display: false // Menyembunyikan legend
                },
                cutoutPercentage: 80, // Membuat chart berbentuk donat
            },
        });

        // Ambil data pendapatan per bulan dari server
        var earningsPerMonth = @json($earningsPerMonth);

        // Menyusun data untuk Chart.js
        var months = [];
        var totals = [];

        earningsPerMonth.forEach(function(item) {
            // Menambahkan bulan dan total pendapatan ke dalam array
            months.push(item.month); // Bulan transaksi (angka 1-12)
            totals.push(item.total); // Total pendapatan per bulan
        });

        // Menyusun label bulan agar lebih mudah dibaca
        var monthNames = ["Jan", "Feb", "Mar", "Apr", "May", "Jun", "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"];
        var monthLabels = months.map(function(month) {
            return monthNames[month - 1]; // Mengubah angka bulan menjadi nama bulan
        });

        // Membuat grafik area
        var ctx = document.getElementById("myAreaChart").getContext("2d");
        var myAreaChart = new Chart(ctx, {
            type: 'line', // Tipe grafik adalah garis
            data: {
                labels: monthLabels, // Menampilkan nama bulan pada sumbu X
                datasets: [{
                    label: 'Earnings',
                    lineTension: 0.3,
                    backgroundColor: "rgba(78, 115, 223, 0.05)",
                    borderColor: "rgba(78, 115, 223, 1)",
                    pointRadius: 3,
                    pointBackgroundColor: "rgba(78, 115, 223, 1)",
                    pointBorderColor: "rgba(78, 115, 223, 1)",
                    pointHoverRadius: 3,
                    pointHoverBackgroundColor: "rgba(78, 115, 223, 1)",
                    pointHoverBorderColor: "rgba(78, 115, 223, 1)",
                    pointHitRadius: 10,
                    pointBorderWidth: 2,
                    data: totals, // Data pendapatan per bulan
                }]
            },
            options: {
                maintainAspectRatio: false,
                layout: {
                    padding: {
                        left: 10,
                        right: 25,
                        top: 25,
                        bottom: 0
                    }
                },
                scales: {
                    xAxes: [{
                        time: {
                            unit: 'date'
                        },
                        gridLines: {
                            display: false,
                            drawBorder: false
                        },
                        ticks: {
                            maxTicksLimit: 7
                        }
                    }],
                    yAxes: [{
                        ticks: {
                            maxTicksLimit: 5,
                            padding: 10,
                            // Menambahkan tanda dolar pada sumbu Y
                            callback: function(value, index, values) {
                                return 'Rp ' + number_format(value);
                            }
                        },
                        gridLines: {
                            color: "rgb(234, 236, 244)",
                            zeroLineColor: "rgb(234, 236, 244)",
                            drawBorder: false,
                            borderDash: [2],
                            zeroLineBorderDash: [2]
                        }
                    }],
                },
                legend: {
                    display: false
                },
                tooltips: {
                    backgroundColor: "rgb(255,255,255)",
                    bodyFontColor: "#858796",
                    titleMarginBottom: 10,
                    titleFontColor: '#6e707e',
                    titleFontSize: 14,
                    borderColor: '#dddfeb',
                    borderWidth: 1,
                    xPadding: 15,
                    yPadding: 15,
                    displayColors: false,
                    intersect: false,
                    mode: 'index',
                    caretPadding: 10,
                    callbacks: {
                        label: function(tooltipItem, chart) {
                            var datasetLabel = chart.datasets[tooltipItem.datasetIndex].label || '';
                            return datasetLabel + ': Rp ' + number_format(tooltipItem.yLabel);
                        }
                    }
                }
            }
        });
    </script>
